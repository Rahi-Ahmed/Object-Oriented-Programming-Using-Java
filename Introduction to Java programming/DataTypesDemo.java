
public class DataTypesDemo {
    public static void main(String[] args) {
        // Primitive data types
        int myInt = 100; // Stores whole numbers
        float myFloat = 5.75f; // Stores fractional numbers and requires 'f' suffix
        double myDouble = 3.14; // Stores fractional numbers with more precision
        char myChar = 'A'; // Stores single characters
        boolean myBool = true; // Stores true or false values

        // Non-primitive data type
        String myString = "Hello Java"; // Stores a sequence of characters

        // Printing the values
        System.out.println("Integer Value: " + myInt);
        System.out.println("Float Value: " + myFloat);
        System.out.println("Double Value: " + myDouble);
        System.out.println("Character Value: " + myChar);
        System.out.println("Boolean Value: " + myBool);
        System.out.println("String Value: " + myString);
    }
}

